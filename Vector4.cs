namespace JollySamurai.UnrealEngine4.T3D
{
    public struct Vector4
    {
        public float X { get; }
        public float Y { get; }
        public float Z { get; }
        public float A { get; }

        public Vector4(float x, float y, float z, float a)
        {
            X = x;
            Y = y;
            Z = z;
            A = a;
        }
    }
}
